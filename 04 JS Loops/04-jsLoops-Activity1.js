// For loop 
for (let i = 0; i <= 15; i++)
{
    if (i % 2 == 0)     //If the number is divisible by 2, it is even.
    {
        document.write(i + " is even<br>"); //We print the number and inform the user that it is even.
    }
    else
    {
        document.write(i + " is odd<br>");  //We print the number and inform the user that it is odd.
    }
}