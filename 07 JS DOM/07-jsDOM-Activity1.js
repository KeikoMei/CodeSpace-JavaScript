function changeStyle() // Function to change the style of the text
{
    let x = document.getElementById("text"); // Get the element with the id "text"
    x.style.backgroundColor = "grey"; // Change the background color of the element
    x.style.color = "purple"; // Change the text color of the element
    x.style.border = "solid"; // Add a border to the element
    x.style.borderColor = "black"; // Change the border color of the element
}


